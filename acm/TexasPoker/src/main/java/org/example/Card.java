package org.example;

public final class  Card {
    private int point;
    private int variety;

    public Card(int p, int v)
    {
        point = p;
        variety = v;
    }

    public int getPoint() {
        return point;
    }

    public void setPoint(int point) {
        this.point = point;
    }

    public int getVariety() {
        return variety;
    }

    public void setVariety(int variety) {
        this.variety = variety;
    }





}
